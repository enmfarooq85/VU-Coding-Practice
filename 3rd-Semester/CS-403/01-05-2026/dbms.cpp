// ER Model

// Entities is a similar concept to classes in OOP. They represent real-world objects or concepts. Each entity has attributes that describe its properties. For example, in a university database, we might have an entity called "Student" with attributes like "Name", "ID", and "Major".

// Relationships represent how entities are related to each other. For example, a "Student" entity might have a relationship with a "Course" entity, indicating that a student can enroll in multiple courses.

// ER diagrams are visual representations of the ER model. They use symbols to represent entities, attributes, and relationships. For example, rectangles represent entities, ovals represent attributes, and diamonds represent relationships. Lines are used to connect these symbols to show how they are related.


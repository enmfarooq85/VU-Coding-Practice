// Iterators in STL

// 1. Input Iterators
// - Used for reading data.
// - Can move only forward.
// - Supports: ++

// 2. Output Iterators
// - Used for writing data.
// - Can move only forward.
// - Supports: ++

// 3. Forward Iterators
// - Can read and write data.
// - Can move only forward.
// - Supports: ++

// 4. Bidirectional Iterators
// - Can move in both directions.
// - Supports: ++ and --

// 5. Random Access Iterators
// - Can access elements directly using indices.
// - Supports: ++, --, +, -, [], <, >, etc.
// - Fastest and most flexible iterator type.

// STL Algorithms

// STL provides 70+ standard algorithms.

